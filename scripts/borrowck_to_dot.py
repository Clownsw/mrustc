def get_links_ivartree(input):
    links = set()
    for line in input.split('\n'):
        if line == "":
            continue
        try:
            _,line = line.split('dump: ')
            if ' = ' in line:
                continue
            varname,extra = line.split(' -- ')
            
            extra = extra[4:-1]
            to,fr = extra.split('], from=[')
            
            if to != "":
                for v in to.split(', '):
                    links.add((varname, v, ""))
            if fr != "":
                for v in fr.split(', '):
                    links.add((v, varname, ""))
        except:
            print(line)
            raise
    return links
